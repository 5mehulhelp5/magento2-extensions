declare(strict_types=1);

namespace Olegnax\InstagramFeedPro\Model\Config\Source;

use Magento\Framework\Option\ArrayInterface;

class IconsStyle implements ArrayInterface
{
    const STYLE_DEFAULT = '-o';
	const STYLE_FILLED	 = '-f';
	/**
     * Get options in "key-value" format
     *
     * @return array
     */
    public function toArray()
    {
        $array = [];
        foreach ($this->toOptionArray() as $item) {
            $array[$item['value']] = $item['label'];
        }
        return $array;
    }

    /**
     * Options getter
     *
     * @return array
     */
    public function toOptionArray()
    {
        return [
            ['value' => static::STYLE_DEFAULT, 'label' => __('Default (Outline)')],
            ['value' => static::STYLE_FILLED, 'label' => __('Filled')],
        ];
    }
}
