namespace Amasty\ImageOptimizer\Model\OptionSource;

use Amasty\PageSpeedTools\Model\OptionSource\ToOptionArrayTrait;
use Magento\Framework\Data\OptionSourceInterface;

class OptimizationType implements OptionSourceInterface
{
    public const IMMEDIATELY = 'immediately';
    public const CRON = 'cron';

    use ToOptionArrayTrait;

    /**
     * Get options in "key-value" format
     *
     * @return array
     */
    public function toArray(): array
    {
        return [
            self::IMMEDIATELY => __('Immediately after Uploading'),
            self::CRON => __('By Cron'),
        ];
    }
}
